fn emit_each_node(
    node: &EachNode,
    state_prefix: &str,
    tap_id: &mut u32,
    tap_actions: &mut Vec<TapAction>,
    indent: usize,
) -> String {
    let pad = "    ".repeat(indent);
    let pad1 = "    ".repeat(indent + 1);

    // The iterator expression — maps to a state field or local var
    let iter_expr = emit_tap_expr(&node.iter, state_prefix);
    let loop_var = format!("__each_{}", node.var);

    // Measure one template child to get per-item height (use first child)
    let item_h = if node.children.is_empty() {
        "0.0".to_string()
    } else {
        // Estimate from first child type
        match &node.children[0] {
            UiNode::Tap(_) => "55.0".to_string(),
            UiNode::Words(w) => format!("{:.1}", text_size(&w.props, 16.0) * 1.4),
            _ => "60.0".to_string(),
        }
    };

    // Emit the draw code for template children, substituting the loop variable
    let mut child_code = String::new();
    for child in &node.children {
        let block = emit_node_block(child, state_prefix, tap_id, tap_actions, indent + 2);
        child_code.push_str(&block.code);
    }

    format!(
        "{pad}// each {var}\n\
         {pad}let __each_{var}_items = {iter};\n\
         {pad}let __each_{var}_h = {item_h};\n\
         {pad}let mut __each_{var}_y = __pb.y;\n\
         {pad}for {loop_var} in __each_{var}_items.iter() {{\n\
         {pad1}let _ = {loop_var};\n\
         {pad1}let __pb = Rect {{ x: __pb.x, y: __each_{var}_y, w: __pb.w, h: __each_{var}_h }};\n\
         {child_code}\
         {pad1}__each_{var}_y += __each_{var}_h;\n\
         {pad}}}\n",
        pad = pad,
        pad1 = pad1,
        var = node.var,
        iter = iter_expr,
        item_h = item_h,
        loop_var = loop_var,
        child_code = child_code,
    )
}

fn emit_show_when(
    node: &ShowWhenNode,
    state_prefix: &str,
    tap_id: &mut u32,
    tap_actions: &mut Vec<TapAction>,
    indent: usize,
) -> String {
    let pad = "    ".repeat(indent);
    let cond_raw = emit_tap_expr(&node.cond, state_prefix);
    // Strip outer parens added by emit_tap_expr for BinOp — avoids Rust
    // unused_parens warning in generated code.
    let cond = if cond_raw.starts_with('(') && cond_raw.ends_with(')') {
        cond_raw[1..cond_raw.len() - 1].to_string()
    } else {
        cond_raw
    };
    // Always render show_when children against the full viewport so that
    // page-level show_when blocks (used for multi-page navigation) fill the
    // entire canvas rather than a 0-height measurement rect.
    let inner = emit_nodes(
        &node.children,
        "Rect { x: 0.0, y: 0.0, w: vw(), h: vh() }",
        state_prefix,
        tap_id,
        tap_actions,
        indent + 1,
    );
    format!("{}if {} {{\n{}{}}}\n", pad, cond, inner, pad)
}

// ─── Tap dispatch generation ─────────────────────────────────────────────────

fn emit_tap_dispatch(actions: &[TapAction]) -> String {
    let mut out = String::new();
    for action in actions {
        out.push_str(&format!("        {} => {{\n", action.id));
        for stmt in &action.body {
            let rust_stmt = emit_tap_stmt(stmt, &action.screen_prefix);
            out.push_str(&format!("            {}\n", rust_stmt));
        }
        out.push_str("        }\n");
    }
    out
}

fn emit_tap_stmt(stmt: &Stmt, prefix: &str) -> String {
    match stmt {
        Stmt::Assign { target, value, .. } => {
            let lhs = match target {
                AssignTarget::Simple(name, _) => {
                    format!("st().{}_{}", prefix, name)
                }
                AssignTarget::Field(obj, field, _) => {
                    format!("st().{}_{}.{}", prefix, emit_expr(obj), field)
                }
                AssignTarget::Index(obj, _idx, _) => {
                    format!("st().{}_{}", prefix, emit_expr(obj))
                }
            };
            let rhs = emit_tap_expr(value, prefix);
            format!("{} = {};", lhs, rhs)
        }
        Stmt::Let { name, value, .. } => {
            format!("let {} = {};", name, emit_tap_expr(value, prefix))
        }
        Stmt::Give { value, .. } => format!("return {};", emit_tap_expr(value, prefix)),
        Stmt::Expr(e) => format!("{};", emit_tap_expr(e, prefix)),
        Stmt::When {
            arms, otherwise, ..
        } => {
            let mut s = String::new();
            for (i, arm) in arms.iter().enumerate() {
                let kw = if i == 0 { "if" } else { "} else if" };
                s.push_str(&format!("{} {} {{ ", kw, emit_tap_expr(&arm.cond, prefix)));
                for b in &arm.body {
                    s.push_str(&emit_tap_stmt(b, prefix));
                    s.push(' ');
                }
                s.push_str("} ");
            }
            if let Some(other) = otherwise {
                s.push_str(&format!("else {{ {} }}", emit_tap_stmt(other, prefix)));
            }
            s
        }
        _ => String::new(),
    }
}

fn emit_tap_expr(expr: &Expr, prefix: &str) -> String {
    match expr {
        Expr::Ident(name, _) => format!("st().{}_{}", prefix, name),
        Expr::Num(n, _) => n.to_string(),
        Expr::Dec(d, _) => format!("{}_f64", d),
        Expr::Bool(b, _) => b.to_string(),
        Expr::Text(parts, _) => {
            let s: String = parts
                .iter()
                .map(|p| match p {
                    TextPart::Literal(s) => s.clone(),
                    TextPart::Interp(e) => format!("{{{}}}", emit_tap_expr(e, prefix)),
                })
                .collect();
            format!("{:?}", s)
        }
        Expr::BinOp {
            left, op, right, ..
        } => {
            let l = emit_tap_expr(left, prefix);
            let r = emit_tap_expr(right, prefix);
            let op_s = match op {
                BinOp::Add => "+",
                BinOp::Sub => "-",
                BinOp::Mul => "*",
                BinOp::Div => "/",
                BinOp::Mod => "%",
                BinOp::Eq => "==",
                BinOp::NotEq => "!=",
                BinOp::Lt => "<",
                BinOp::Gt => ">",
                BinOp::LtEq => "<=",
                BinOp::GtEq => ">=",
                BinOp::And => "&&",
                BinOp::Or => "||",
                _ => "+",
            };
            format!("({} {} {})", l, op_s, r)
        }
        Expr::Not { expr, .. } => format!("(!{})", emit_tap_expr(expr, prefix)),
        Expr::Neg { expr, .. } => format!("(-{})", emit_tap_expr(expr, prefix)),
        _ => emit_expr(expr),
    }
}

// ─── Render dispatch ─────────────────────────────────────────────────────────

fn emit_render_dispatch(screens: &[&ScreenDef]) -> String {
    if screens.is_empty() {
        return "// no screens".to_string();
    }

    // Clear with background fill of first screen's root box
    let bg = screens
        .first()
        .and_then(|s| s.children.first())
        .and_then(|n| {
            if let UiNode::Box(b) = n {
                b.props.fill.as_ref().map(|f| {
                    let css = fill_expr_to_css(f);
                    hex_to_rgb(&css).unwrap_or((10, 10, 10))
                })
            } else {
                None
            }
        })
        .unwrap_or((10, 10, 10));

    let mut out = format!(
        "unsafe {{ ved_clear({}, {}, {}); }}\n    ",
        bg.0, bg.1, bg.2
    );

    if screens.len() == 1 {
        out.push_str(&format!("render_{}();", screens[0].name.to_lowercase()));
    } else {
        out.push_str("match st().screen {\n");
        for (i, screen) in screens.iter().enumerate() {
            out.push_str(&format!(
                "        {} => render_{}(),\n",
                i,
                screen.name.to_lowercase()
            ));
        }
        out.push_str("        _ => {}\n    }");
    }
    out
}

// ─── Shape structs ───────────────────────────────────────────────────────────

fn emit_shape_struct(shape: &ShapeDef) -> String {
    let fields: Vec<String> = shape
        .fields
        .iter()
        .map(|f| {
            let ty = match &f.ty {
                TypeExpr::Num => "i64",
                TypeExpr::Dec => "f64",
                TypeExpr::Bool => "bool",
                TypeExpr::Text => "&'static str",
                _ => "i64",
            };
            format!("    pub {}: {}", f.name, ty)
        })
        .collect();
    format!(
        "#[derive(Clone, Copy, Debug)]\nstruct {} {{\n{}\n}}\n",
        shape.name,
        fields.join(",\n")
    )
}

// ─── Think functions ─────────────────────────────────────────────────────────

fn emit_think_fn(think: &ThinkDef, tap_id: &mut u32, tap_actions: &mut Vec<TapAction>) -> String {
    let params: Vec<String> = think
        .params
        .iter()
        .map(|p| {
            let ty = match &p.ty {
                TypeExpr::Num => "i64",
                TypeExpr::Dec => "f64",
                TypeExpr::Bool => "bool",
                _ => "i64",
            };
            format!("{}: {}", p.name, ty)
        })
        .collect();

    let mut out = String::new();

    // If there are UI children, emit a render_piece_{name} function
    if !think.ui_children.is_empty() {
        let param_str = params.join(", ");
        let param_names: Vec<&str> = params
            .iter()
            .map(|p| p.split(':').next().unwrap_or("_").trim())
            .collect();
        let node_code = emit_nodes(
            &think.ui_children,
            "Rect { x: 0.0, y: 0.0, w: vw(), h: vh() }",
            "",
            tap_id,
            tap_actions,
            1,
        );
        out.push_str(&format!(
            "fn render_piece_{}({}) {{\n    let _ = ({});\n{}}}\n",
            think.name.to_lowercase(),
            param_str,
            param_names.join(", "),
            node_code,
        ));
    }

    // If there are logic stmts (or no UI children at all), emit a regular fn
    let has_real_stmts = think
        .body
        .iter()
        .any(|s| !matches!(s, Stmt::Expr(Expr::Nothing(_))));
    if has_real_stmts || think.ui_children.is_empty() {
        let ret = match &think.ret {
            Some(TypeExpr::Num) => "i64",
            Some(TypeExpr::Dec) => "f64",
            Some(TypeExpr::Bool) => "bool",
            None => "()",
            _ => "i64",
        };
        let body: Vec<String> = think
            .body
            .iter()
            .filter(|s| !matches!(s, Stmt::Expr(Expr::Nothing(_))))
            .map(|s| format!("    {}", wasm_emit_stmt(s, 1)))
            .collect();
        out.push_str(&format!(
            "fn {}({}) -> {} {{\n{}\n}}\n",
            think.name,
            params.join(", "),
            ret,
            body.join("\n")
        ));
    }

    out
}

// ─── WASM-safe statement emitter ────────────────────────────────────────────
// The shared emit_stmt maps println/print → println!() which doesn't exist in
// no_std. Suppress those and any other std-only calls in the WASM target.
fn wasm_emit_stmt(stmt: &Stmt, indent: usize) -> String {
    let pad = "    ".repeat(indent);
    // Bare identifier: `println;` `print;` etc.
    if let Stmt::Expr(Expr::Ident(name, _)) = stmt {
        if matches!(name.as_str(), "println" | "print" | "input" | "exit") {
            return format!("{}// {} suppressed (no stdout in wasm32)", pad, name);
        }
    }
    // Call: println(...) / print(...)
    if let Stmt::Expr(Expr::Call { func, .. }) = stmt {
        if let Expr::Ident(name, _) = func.as_ref() {
            if matches!(name.as_str(), "println" | "print" | "input" | "exit") {
                return format!("{}// {} suppressed (no stdout in wasm32)", pad, name);
            }
        }
    }
    // Bare string literal — leaked argument from a suppressed println/print call.
    if let Stmt::Expr(Expr::Text(_, _)) = stmt {
        return format!("{}// string suppressed (no stdout in wasm32)", pad);
    }
    emit_stmt(stmt, indent)
}

