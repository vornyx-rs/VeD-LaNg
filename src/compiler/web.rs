// ─── Public entry point ───────────────────────────────────────────────────────

pub fn emit(program: Program, out: &Path, release: bool) -> CompileResult<()> {
    std::fs::create_dir_all(out).map_err(|e| crate::compiler::CompileError {
        message: format!("Failed to create output dir: {}", e),
        span: (0..0).into(),
    })?;

    // Derive lib name before program is moved
    let lib_name = find_main_screen(&program)
        .map(|s| s.name.to_lowercase().replace('-', "_"))
        .unwrap_or_else(|| "ved_app".to_string());

    // 1. HTML bootstrap (replaces browser, wires WASM imports)
    let html = generate_html_bootstrap(&program);
    write_file(out.join("index.html"), &html)?;

    // 2. Cargo.toml for the WASM project
    let cargo_toml = generate_wasm_cargo_toml(&program);
    write_file(out.join("Cargo.toml"), &cargo_toml)?;

    // 3. Rust source that compiles to app.wasm
    let src_dir = out.join("src");
    std::fs::create_dir_all(&src_dir).map_err(|e| crate::compiler::CompileError {
        message: format!("Failed to create src dir: {}", e),
        span: (0..0).into(),
    })?;

    let rust_src = generate_wasm_source(&program);
    write_file(src_dir.join("lib.rs"), &rust_src)?;

    // 4. Ghost intents artifact (unchanged)
    let intents = generate_ghost_intents(&program);
    if !intents.is_empty() {
        write_file(out.join("ghost-intents.json"), &intents)?;
    }

    println!("  web  ->  {}/src/lib.rs", out.display());

    // 5. Invoke cargo to compile the generated Rust to WASM
    let profile = if release { "release" } else { "debug" };
    let mut cargo_args = vec!["build", "--target", "wasm32-unknown-unknown"];
    if release {
        cargo_args.push("--release");
    }

    match std::process::Command::new("cargo")
        .args(&cargo_args)
        .current_dir(out)
        .status()
    {
        Ok(status) if status.success() => {
            let wasm_src = out.join(format!(
                "target/wasm32-unknown-unknown/{}/{}.wasm",
                profile, lib_name
            ));
            if wasm_src.exists() {
                std::fs::copy(&wasm_src, out.join("app.wasm")).map_err(|e| {
                    crate::compiler::CompileError {
                        message: format!("Failed to copy app.wasm: {}", e),
                        span: (0..0).into(),
                    }
                })?;
                println!("  wasm ->  {}/app.wasm", out.display());
            } else {
                eprintln!(
                    "  warning: cargo succeeded but {} not found",
                    wasm_src.display()
                );
            }
        }
        Ok(status) => {
            eprintln!(
                "  warning: WASM build failed (exit {}). Ensure the wasm32-unknown-unknown target is installed:",
                status
            );
            eprintln!("    rustup target add wasm32-unknown-unknown");
        }
        Err(e) => {
            eprintln!("  warning: could not invoke cargo ({})", e);
            eprintln!(
                "  To build WASM manually: cd {} && cargo build --target wasm32-unknown-unknown",
                out.display()
            );
        }
    }

    Ok(())
}

