// ─── String bridge: pass &str to JS without allocator overhead ───────────────
static mut STR_BUF: [u8; 8192] = [0u8; 8192];
unsafe fn with_str<R, F: Fn(*const u8, u32) -> R>(s: &str, f: F) -> R {{
    let b = s.as_bytes();
    let n = if b.len() < STR_BUF.len() {{ b.len() }} else {{ STR_BUF.len() - 1 }};
    let dst = &mut STR_BUF[..n];
    dst.copy_from_slice(&b[..n]);
    f(STR_BUF.as_ptr(), n as u32)
}}

