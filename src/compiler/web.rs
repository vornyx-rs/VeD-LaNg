fn find_main_screen(program: &Program) -> Option<&ScreenDef> {
    program.items.iter().find_map(|i| {
        if let Item::Screen(s) = i {
            Some(s)
        } else {
            None
        }
    })
}

fn fill_expr_to_css(expr: &Expr) -> String {
    match expr {
        Expr::Color(c, _) => {
            if c.starts_with('#') {
                c.clone()
            } else {
                format!("#{}", c)
            }
        }
        Expr::Ident(s, _) => s.clone(),
        Expr::Text(parts, _) => {
            let s: String = parts
                .iter()
                .map(|p| match p {
                    TextPart::Literal(l) => l.clone(),
                    _ => String::new(),
                })
                .collect();
            if s.starts_with('#') {
                s
            } else {
                format!("#{}", s)
            }
        }
        _ => "#000000".to_string(),
    }
}

fn color_to_css(color: &ColorExpr) -> String {
    match color {
        ColorExpr::Hex(h) => {
            if h.starts_with('#') {
                h.clone()
            } else {
                format!("#{}", h)
            }
        }
        ColorExpr::Named(n) => n.clone(),
        ColorExpr::Fade(a, _b) => a.clone(),
    }
}

fn hex_to_rgb(hex: &str) -> Option<(u8, u8, u8)> {
    let h = hex.trim_start_matches('#');
    if h.len() == 6 {
        let r = u8::from_str_radix(&h[0..2], 16).ok()?;
        let g = u8::from_str_radix(&h[2..4], 16).ok()?;
        let b = u8::from_str_radix(&h[4..6], 16).ok()?;
        Some((r, g, b))
    } else if h.len() == 3 {
        let r = u8::from_str_radix(&h[0..1].repeat(2), 16).ok()?;
        let g = u8::from_str_radix(&h[1..2].repeat(2), 16).ok()?;
        let b = u8::from_str_radix(&h[2..3].repeat(2), 16).ok()?;
        Some((r, g, b))
    } else {
        None
    }
}

fn text_size(props: &TextProps, default: f64) -> f64 {
    match &props.size {
        Some(Expr::Num(n, _)) => *n as f64,
        Some(Expr::Dec(d, _)) => *d,
        _ => default,
    }
}

fn text_weight_str(weight: &Option<TextWeight>) -> &'static str {
    match weight {
        Some(TextWeight::Fat) => "700",
        Some(TextWeight::Thin) => "300",
        _ => "400",
    }
}

fn prop_f64(expr: &Option<Expr>, default: f64) -> f64 {
    match expr {
        Some(Expr::Num(n, _)) => *n as f64,
        Some(Expr::Dec(d, _)) => *d,
        _ => default,
    }
}

fn pad_h(padding: &Option<PaddingExpr>, default: f64) -> f64 {
    padding.as_ref().map(|p| p.right as f64).unwrap_or(default)
}

fn pad_v(padding: &Option<PaddingExpr>, default: f64) -> f64 {
    padding.as_ref().map(|p| p.top as f64).unwrap_or(default)
}

/// Convert a VED expression to a raw Rust display value (for use inside format! args).
/// Returns an expression that implements Display — NOT wrapped in Cow.
fn expr_to_display(expr: &Expr, state_prefix: &str) -> String {
    match expr {
        Expr::Num(n, _) => n.to_string(),
        Expr::Dec(d, _) => format!("{d}f64"),
        Expr::Bool(b, _) => b.to_string(),
        Expr::Ident(name, _) => {
            if !state_prefix.is_empty() {
                format!("st().{}_{}", state_prefix, name)
            } else {
                "\"\"".to_string()
            }
        }
        Expr::Text(parts, _) => {
            let all_literal = parts.iter().all(|p| matches!(p, TextPart::Literal(_)));
            if all_literal {
                let s: String = parts
                    .iter()
                    .map(|p| match p {
                        TextPart::Literal(s) => s.clone(),
                        _ => String::new(),
                    })
                    .collect();
                format!("{:?}", s)
            } else {
                let mut fmt = String::new();
                let mut args: Vec<String> = Vec::new();
                for part in parts {
                    match part {
                        TextPart::Literal(s) => {
                            fmt.push_str(&s.replace('{', "{{").replace('}', "}}"))
                        }
                        TextPart::Interp(e) => {
                            fmt.push_str("{}");
                            args.push(expr_to_display(e, state_prefix));
                        }
                    }
                }
                format!("format!({:?}, {})", fmt, args.join(", "))
            }
        }
        _ => "\"\"".to_string(),
    }
}

/// Convert a VED expression to a `Cow<str>` Rust expression for canvas text APIs.
fn expr_to_runtime(expr: &Expr, state_prefix: &str) -> String {
    match expr {
        Expr::Text(parts, _) => {
            let all_literal = parts.iter().all(|p| matches!(p, TextPart::Literal(_)));
            if all_literal {
                let s: String = parts
                    .iter()
                    .map(|p| match p {
                        TextPart::Literal(s) => s.clone(),
                        _ => String::new(),
                    })
                    .collect();
                format!("alloc::borrow::Cow::Borrowed({:?})", s)
            } else {
                let mut fmt = String::new();
                let mut args: Vec<String> = Vec::new();
                for part in parts {
                    match part {
                        TextPart::Literal(s) => {
                            fmt.push_str(&s.replace('{', "{{").replace('}', "}}"))
                        }
                        TextPart::Interp(e) => {
                            fmt.push_str("{}");
                            // Use display variant so we don't double-wrap in Cow
                            args.push(expr_to_display(e, state_prefix));
                        }
                    }
                }
                format!(
                    "alloc::borrow::Cow::Owned(format!({:?}, {}))",
                    fmt,
                    args.join(", ")
                )
            }
        }
        Expr::Num(n, _) => format!("alloc::borrow::Cow::Borrowed(\"{n}\")"),
        Expr::Dec(d, _) => format!("alloc::borrow::Cow::Owned(format!(\"{{:.2}}\", {d}f64))"),
        Expr::Bool(b, _) => format!("alloc::borrow::Cow::Borrowed(\"{b}\")"),
        Expr::Ident(name, _) => {
            if !state_prefix.is_empty() {
                format!(
                    "alloc::borrow::Cow::Owned(format!(\"{{}}\", st().{}_{}))",
                    state_prefix, name
                )
            } else {
                "alloc::borrow::Cow::Borrowed(\"\")".to_string()
            }
        }
        _ => "alloc::borrow::Cow::Borrowed(\"\")".to_string(),
    }
}

fn unique_id(node: &BoxNode) -> u64 {
    // Use pointer address as unique id for variable naming
    node as *const _ as u64
}

fn write_file(path: impl AsRef<Path>, content: &str) -> CompileResult<()> {
    std::fs::write(&path, content).map_err(|e| crate::compiler::CompileError {
        message: format!("Failed to write {}: {}", path.as_ref().display(), e),
        span: (0..0).into(),
    })
}

// ─── Ghost intents (unchanged) ───────────────────────────────────────────────

fn generate_ghost_intents(program: &Program) -> String {
    let mut out = String::from("{\n  \"screens\": [\n");
    let mut first = true;
    for item in &program.items {
        let Item::Screen(screen) = item else { continue };
        if !screen.ghost {
            continue;
        }
        if !first {
            out.push_str(",\n");
        }
        first = false;
        out.push_str(&format!(
            "    {{ \"name\": \"{}\", \"intents\": [{}] }}",
            json_escape(&screen.name),
            screen
                .intents
                .iter()
                .map(|i| format!("\"{}\"", json_escape(i)))
                .collect::<Vec<_>>()
                .join(", ")
        ));
    }
    if first {
        return String::new();
    }
    out.push_str("\n  ]\n}\n");
    out
}

fn json_escape(s: &str) -> String {
    s.replace('\\', "\\\\")
        .replace('"', "\\\"")
        .replace('\n', "\\n")
}
